package javaNotes;

public class AllVariables {
	//variables are used to store data it can be number, char,String,decimal number etc
	public static void main(String[] args) {
		//*for Number
		byte a=12;// can store upto 8bits
		short b=69;// can store upto 2bytes
		int c=100;// can stor upto 4bytes
		long d= 1243444222111111111l;//l is mandatory after it exceeds certain value *it can store 8bytes
		
		//*for decimal number
		double e=12.34;// d is mandatory after very very big values only
		float f=12.23f;// written f is mandatory in float type 
		
		//*for character
		char g='e';//we can store only 1 character
		String h= "hello";//always used for collection of one or more then one characters
		
		
		
	}

}
